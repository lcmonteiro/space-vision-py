# filters
from vision.library.filters import TextFilter

# ------------------------------------------------------------------------------------------------
# Filter - Builder
# ------------------------------------------------------------------------------------------------
class VisionConfigFilter:
    @staticmethod
    def Build(id, config):
        # ---------------------------------------------------------------------
        # Register
        # ---------------------------------------------------------------------
        FILTERS = {
            "text": lambda conf : TextFilter(conf)
        }
        # ---------------------------------------------------------------------
        # Process
        # ---------------------------------------------------------------------
        return FILTERS[id](config)

