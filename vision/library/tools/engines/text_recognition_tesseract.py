# external
from pytesseract import image_to_string

# ################################################################################################
# ------------------------------------------------------------------------------------------------
# TextRecognitionTesseract 
# ------------------------------------------------------------------------------------------------
# ################################################################################################
class TextRecognitionTesseract:
    #
    # -------------------------------------------------------------------------
    # initialization
    # -------------------------------------------------------------------------
    #
    def __init__(self):
        super().__init__()
        # configuration
        self.__config = ("-l eng --oem 1 --psm 7")
    #
    # -------------------------------------------------------------------------
    # process
    # -------------------------------------------------------------------------
    #
    def process(self, frame):
        # format results
        return image_to_string(frame, config=self.__config)
    
