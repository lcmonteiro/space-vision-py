from .engines        import TextRecognitionTesseract
# internal
from vision.library  import VisionTool

# ################################################################################################
# ------------------------------------------------------------------------------------------------
# TextDetection 
# ------------------------------------------------------------------------------------------------
# ################################################################################################
class TextRecognitionTool(VisionTool):
    # -------------------------------------------------------------------------
    # initialization
    # -------------------------------------------------------------------------
    def __init__(self, engine='tesseract'):
        super().__init__()
        # engines
        self.__engine = {
            'tesseract': TextRecognitionTesseract
        }[engine]()
    # -------------------------------------------------------------------------
    # process
    # -------------------------------------------------------------------------
    def process(self, frame):
        return self.__engine.process(frame)
