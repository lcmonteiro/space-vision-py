from vision.library.tools.engines  import TextDetectionEAST
from vision.library                import VisionTool

# ################################################################################################
# ------------------------------------------------------------------------------------------------
# TextDetection 
# ------------------------------------------------------------------------------------------------
# ################################################################################################
class TextDetectionTool(VisionTool):
    # -------------------------------------------------------------------------
    # initialization
    # -------------------------------------------------------------------------
    def __init__(self, engine='east'):
        super().__init__()
        # engines
        self.__engine = {
            'east': TextDetectionEAST
        }[engine]()
    # -------------------------------------------------------------------------
    # process
    # -------------------------------------------------------------------------
    def process(self, frame):
        return self.__engine.process(frame)
