# external
from re              import match

# internal
from vision.library  import VisionQuery

# ################################################################################################
# ---------------------------------------------------------------------------------------
# VisionFilterText
# ---------------------------------------------------------------------------------------
# ################################################################################################
class FindTextQuery(VisionQuery):

    # -------------------------------------------------------------------------
    # initialization
    # -------------------------------------------------------------------------
    def __init__(self, db):
        super().__init__(db)
    
    # -------------------------------------------------------------------------
    # process
    # -------------------------------------------------------------------------
    def __call__(self, colection, time_interval, label_pattern):
        def process(result):
            if match(label_pattern, result.label()):
                return result.label()
            return None
        return self.process(time_interval, process)
         
