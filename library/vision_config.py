from library.filters.vision_filter_number import VisionFilterNumber
#
# ------------------------------------------------------------------------------------------------
# Filter - Builder
# ------------------------------------------------------------------------------------------------
#
class VisionFilterBuilder:
    @staticmethod
    def Build(id, config):
        #
        # ---------------------------------------------------------------------
        # Register
        # ---------------------------------------------------------------------
        #
        FILTERS = {
        	"number": lambda conf : VisionFilterNumber(conf)
        }
        #
        # ---------------------------------------------------------------------
        # Process
        # ---------------------------------------------------------------------
        #/
        return FILTERS[id](config)
