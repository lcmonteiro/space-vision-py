from library.filters import VisionFilterText
#
# ------------------------------------------------------------------------------------------------
# Filter - Builder
# ------------------------------------------------------------------------------------------------
#
class VisionFilterBuilder:
    @staticmethod
    def Build(id, config):
        # ---------------------------------------------------------------------
        # Register
        # ---------------------------------------------------------------------
        FILTERS = {
            "text": lambda conf : VisionFilterText(conf)
        }
        # ---------------------------------------------------------------------
        # Process
        # ---------------------------------------------------------------------
        return FILTERS[id](config)

